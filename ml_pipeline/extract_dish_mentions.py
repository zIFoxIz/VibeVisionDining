# ---------------------------------------------------------------------------
# Sentiment model loader
# ---------------------------------------------------------------------------

class SentimentClassifier:
    """Unified wrapper — uses sklearn pkl if present, else minimal JSON."""

    def __init__(self):
        if os.path.exists(PKL_MODEL) and os.path.exists(PKL_VECTORIZER):
            import pickle
            with open(PKL_MODEL, 'rb') as f:
                self._model = pickle.load(f)
            with open(PKL_VECTORIZER, 'rb') as f:
                self._vec = pickle.load(f)
            self._mode = 'sklearn'
            print("  Sentiment: using full sklearn pkl model.")
        elif os.path.exists(MINIMAL_JSON):
            with open(MINIMAL_JSON, 'r') as f:
                data = json.load(f)
            self._classes   = data['classes']          # ['negative','neutral','positive']
            self._features  = data['top_features']
            self._feat_idx  = {w: i for i, w in enumerate(self._features)}
            self._coef      = data['coef']             # shape (n_classes, n_features)
            self._intercept = data['intercept']
            self._mode = 'minimal'
            print("  Sentiment: using minimal JSON model (500 features).")
        else:
            self._mode = 'none'
            print("  WARNING: no sentiment model found — all mentions default to positive.")

    def predict(self, text: str) -> str:
        """Return 'positive', 'neutral', or 'negative'."""
        if self._mode == 'none':
            return 'positive'
        if self._mode == 'sklearn':
            return str(self._model.predict(self._vec.transform([text]))[0])
        # Minimal JSON inference: unigrams + bigrams matched against top_features
        tokens = re.sub(r'[^a-z\s]', ' ', text.lower()).split()
        ngrams = tokens + [f"{tokens[i]} {tokens[i+1]}" for i in range(len(tokens) - 1)]
        scores = list(self._intercept)
        for ng in ngrams:
            idx = self._feat_idx.get(ng)
            if idx is not None:
                for cls_i, coef_row in enumerate(self._coef):
                    scores[cls_i] += coef_row[idx]
        return self._classes[scores.index(max(scores))]


def sentences_containing(text: str, keyword: str) -> list[str]:
    """Split text into sentences and return those mentioning keyword."""
    parts = re.split(r'(?<=[.!?])\s+', text)
    kw_lower = keyword.lower()
    return [s for s in parts if kw_lower in s.lower()]


