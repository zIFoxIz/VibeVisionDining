def cuisine_keywords_for(category_str: str) -> set[str]:
    cat = (category_str or '').lower()
    matched: set[str] = set()
    for key, kws in CUISINE_KEYWORDS.items():
        if key in cat:
            matched.update(kws)
    return matched if matched else GENERIC_FOOD_KEYWORDS


def extract_dishes(text: str, vocab: set[str]) -> list[str]:
    """Return all keyword hits found in text (multi-word keywords handled)."""
    lower = text.lower()
    return [kw for kw in vocab if re.search(r'\b' + re.escape(kw) + r'\b', lower)]


def normalize_key(name: str, city: str) -> str:
    def clean(s: str) -> str:
        return re.sub(r'[^a-z0-9 ]', '', s.lower()).strip()
    return f"{clean(name)}|{clean(city)}"


# ---------------------------------------------------------------------------
# Step 1 – collect restaurant business_ids and their cuisine strings
# ---------------------------------------------------------------------------
print("Step 1/4 – scanning businesses …")
# business_id -> (name, city, categories)
restaurant_info: dict[str, tuple[str, str, str]] = {}

with open(BUSINESS_PATH, 'r', encoding='utf-8') as f:
    for line in f:
        line = line.strip()
        if not line:
            continue
        biz = json.loads(line)
        cats = (biz.get('categories') or '')
        if 'restaurant' in cats.lower():
            bid  = biz.get('business_id')
            name = (biz.get('name') or '').strip()
            city = (biz.get('city') or '').strip()
            if bid and name:
                restaurant_info[bid] = (name, city, cats)

print(f"  Found {len(restaurant_info):,} restaurant businesses.")

# ---------------------------------------------------------------------------
# Step 2 – load sentiment model
# ---------------------------------------------------------------------------
print("Step 2/4 – loading sentiment model …")
clf = SentimentClassifier()

# ---------------------------------------------------------------------------
# Step 3 – scan reviews, extract dish mentions and classify each sentence
# ---------------------------------------------------------------------------
print(f"Step 3/4 – scanning up to {MAX_REVIEWS:,} reviews …")

# { business_id: { dish: { 'positive': N, 'neutral': N, 'negative': N } } }
dish_sentiment: dict[str, dict[str, dict[str, int]]] = \
    defaultdict(lambda: defaultdict(lambda: {'positive': 0, 'neutral': 0, 'negative': 0}))


scanned = 0
with open(REVIEW_PATH, 'r', encoding='utf-8') as f:
    for line in f:
        line = line.strip()
        if not line:
            continue
        review = json.loads(line)
        bid = review.get('business_id')
        if bid not in restaurant_info:
            continue

        text = (review.get('text') or '').strip()
        if not text:
            continue

        vocab = cuisine_keywords_for(restaurant_info[bid][2])
        for dish in extract_dishes(text, vocab):
            for sent in sentences_containing(text, dish):
                label = clf.predict(sent)
                dish_sentiment[bid][dish][label] += 1

        scanned += 1
        if scanned % 50000 == 0:
            print(f"  … {scanned:,} reviews processed, "
                  f"{len(dish_sentiment):,} restaurants with dish data so far")
        if scanned >= MAX_REVIEWS:
            break

print(f"  Processed {scanned:,} reviews across {len(dish_sentiment):,} restaurants.")

# ---------------------------------------------------------------------------
# Step 4 – build output: top-N dishes ranked by sentiment score
# ---------------------------------------------------------------------------
print(f"Step 4/4 – writing output (top {TOP_DISHES} dishes per restaurant) …")

output: dict[str, list[dict]] = {}
for bid, dishes in dish_sentiment.items():
    info = restaurant_info.get(bid)
    if not info:
        continue
    name, city, _ = info
    key = normalize_key(name, city)

    # Rank by (positive - negative), then by total mentions as tiebreak
    ranked = sorted(
        dishes.items(),
        key=lambda x: (
            x[1]['positive'] - x[1]['negative'],
            x[1]['positive'] + x[1]['neutral'] + x[1]['negative'],
        ),
        reverse=True,
    )[:TOP_DISHES]

    if ranked:
        output[key] = [
            {
                'dish':     dish.title(),
                'positive': counts['positive'],
                'neutral':  counts['neutral'],
                'negative': counts['negative'],
            }
            for dish, counts in ranked
        ]

with open(OUTPUT_PATH, 'w', encoding='utf-8') as f:
    json.dump(output, f, indent=2)

