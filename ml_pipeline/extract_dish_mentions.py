"""
VibeVision Dining - Dish Mention + Sentiment Extractor
=======================================================
Scans the Yelp Open Dataset, extracts dish mentions per restaurant, and
classifies each mention sentence with the trained sentiment model to
produce positive/neutral/negative counts per dish.

Output: dish_mentions_by_business.json
  {
    "<normalized_name>|<normalized_city>": [
      { "dish": "Pizza", "positive": 14, "neutral": 3, "negative": 1 },
      ...
    ],
    ...
  }

  Key format: lowercase alphanumeric+spaces, pipe-separated name and city.

Sentiment model priority:
  1. sentiment_model.pkl + vectorizer.pkl  (full sklearn model, generated by
     train_sentiment_model.py — highest accuracy)
  2. sentiment_model_minimal.json          (500-feature logistic regression
     shipped with the app — works without running training first)

Run:
    python extract_dish_mentions.py

Optional env vars:
    YELP_MAX_REVIEWS  - max reviews to scan (default 500000)
    TOP_DISHES        - max dishes per restaurant (default 6)
"""

